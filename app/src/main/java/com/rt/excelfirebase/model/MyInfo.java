package com.rt.excelfirebase.model;

public class MyInfo {
    String name;
    String hobby;

    public MyInfo() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getHobby() {
        return hobby;
    }

    public void setHobby(String hobby) {
        this.hobby = hobby;
    }

    public MyInfo(String name, String hobby) {
        this.name = name;
        this.hobby = hobby;
    }
}
